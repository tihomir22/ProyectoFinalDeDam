    private void botonGenerarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonGenerarActionPerformed
        String contenido = this.textoPalabra.getText();
        if (contenido.length() == 0) {
            JOptionPane.showMessageDialog(rootPane, "Tienes que introducir una palabra a buscar!");
        }else{
            if(this.rutaFich!=null){
                try {
                     
                    String res=modelo.Anagrama.anagrama(contenido, this.rutaFich);
                    JTextArea textArea = new JTextArea(6, 25);
                    textArea.setText(res);
                    textArea.setEditable(false);
                    JScrollPane scrollPane = new JScrollPane(textArea);
                    JOptionPane.showMessageDialog(rootPane, scrollPane);
                } catch (FileNotFoundException ex) {
                    Logger.getLogger(anagrama.class.getName()).log(Level.SEVERE, null, ex);
                }
            }else{
                JOptionPane.showMessageDialog(rootPane, "Tienes que introducir una ruta de archivo!");
            }
        }
    }//GEN-LAST:event_botonGenerarActionPerformed

