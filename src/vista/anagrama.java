    private void botonFicheroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_botonFicheroActionPerformed
        JFileChooser fc = new JFileChooser();
        fc.setVisible(true);
        int returnVal = fc.showOpenDialog(jLabel1);
        if (returnVal == JFileChooser.APPROVE_OPTION) {
            File file = fc.getSelectedFile();
            this.rutaFich=file;
            this.botonFichero.setText(file.getName());
            
            //This is where a real application would open the file.
            System.out.println("Abriendo archivo" + file.getName());
        } else {
            System.out.println("Cancelado por usuario");
        }
    }//GEN-LAST:event_botonFicheroActionPerformed

